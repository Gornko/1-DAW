public class Ejer01 {

    public static void main(String[] args) {

        //Ejercicio resuelto con for
        for (int i = 48; i < 101; i = i + 2) {
            System.out.print(i + " ");
        }
        System.out.println("");

        //Ejercicio resuelto con while
        int num = 48;
        while (num < 101) {
            System.out.print(num + " ");
            num= num+ 2;
        }
    }
}
